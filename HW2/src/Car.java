public class Car {

	private String vin, make;
	private int year;
	
	public Car(String vin, String make, int year) {
		this.vin = vin;
		this.make = make;
		this.year = year;
	}
	
	public String getVin() {
		return vin;
	}
	
	public String getMake() {
		return make;
	}
	
}
